class QalcSignals(QObject):

    progress = pyqtSignal(int)
    finished = pyqtSignal(str)
    gdal_finished = pyqtSignal(str, float, float, float)
    error = pyqtSignal(str)


class OSMWorker(QRunnable):
    def __init__(self, south, west, north, east, output_directory):
        super().__init__()
        self.signals = QalcSignals()
        self.south = south
        self.west = west
        self.north = north
        self.east = east
        self.output_directory = output_directory

    def run(self):

        self.signals.progress.emit(0)

        bbox = f"{self.south},{self.west},{self.north},{self.east}"
        over_ql = f"""
        [out:xml][timeout:120];
        (
            way["building"]({bbox});
            relation["building"]({bbox});
        );
        (._; >;);
        out body qt;
        """

        endpoints = [
            "https://overpass-api.de/api/interpreter",
            "https://maps.mail.ru/osm/tools/overpass/api/interpreter",
            "https://overpass.private.coffee/api/interpreter"
            ]

        # url = "https://overpass-api.de/api/interpreter"
        output_file = os.path.join(self.output_directory, "aoi_data.osm")
        data = urllib.parse.urlencode({"data": over_ql}).encode("utf-8")
        try:
            for url in endpoints:
                req = urllib.request.Request(
                    url, data=data, headers={"User-Agent": "QGIS-Qalc-Plugin/1.0"}
                )
                with urllib.request.urlopen(req, timeout=130) as response:
                    with open(output_file, "wb") as f:
                        block_size = 1024 * 8
                        while True:
                            buffer = response.read(block_size)
                            if not buffer:
                                break
                            f.write(buffer)
                self.signals.finished.emit(output_file)
                return
        except Exception as e:
            self.signals.error.emit(f"overpass api error: {str(e)}")

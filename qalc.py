# GDAL worker
class GDALWorker(QRunnable):
    def __init__(
        self, height_input_path, aoi_extent, grid_res, output_dir, albedo_layer
    ):
        super().__init__()
        self.signals = QalcSignals()
        self.height_input_path = height_input_path
        self.aoi_extent = aoi_extent
        self.grid_res = grid_res
        self.output_dir = output_dir
        self.albedo_layer = albedo_layer

    def run(self):
        try:
            self.signals.progress.emit(0)
            proj_win = [
                self.aoi_extent.xMinimum(),
                self.aoi_extent.yMaximum(),
                self.aoi_extent.xMaximum(),
                self.aoi_extent.yMinimum(),
            ]

            cropped_path = f"/vsimem/heightmap_crop.tif"

            translate_opts = gdal.TranslateOptions(
                projWin=proj_win,
                width=self.grid_res,
                height=self.grid_res,
                resampleAlg=gdal.GRIORA_Bilinear,
                creationOptions=["COMPRESS=NONE", "BIGTIFF=IF_NEEDED"],
            )

            ds = gdal.Translate(
                cropped_path, self.height_input_path, options=translate_opts
            )
            if ds is not None:
                ds.FlushCache()

                self.signals.progress.emit(0)
                gtif = gdal.Open(cropped_path)
                elevation_band = gtif.GetRasterBand(1)
                stats = elevation_band.GetStatistics(True, True)
                min_elev = stats[0]
                max_elev = stats[1]
                elev_range = max_elev - min_elev
                gtif = None


            final_path = os.path.join(self.output_dir, "heightmap.png")
            translate_opts = gdal.TranslateOptions(
                format="PNG",
                outputType=gdal.GDT_UInt16,
                scaleParams=[[min_elev, max_elev, 0, 65535]],
            )
            ds = gdal.Translate(final_path, cropped_path, options=translate_opts)
            if ds is not None:
                ds.FlushCache()

            self.signals.gdal_finished.emit(cropped_path, min_elev, max_elev, elev_range)

        except Exception as e:
            QMessageBox.critical(
                self.iface.mainWindow(), "dang", f"height export failed: {str(e)}"
            )



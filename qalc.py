    def albedo_export(
        self,
        aoi_extent,
        albedo_layer,
        z_scale,
        min_elev,
        max_elev,
        elev_range,
        grid_res,
    ):
        # basemap clip -> albedo.png, for texture in ue material
        final_path = os.path.join(
            self.selected_directory, f"{albedo_layer.name()}_albedo.png"
        )
        grid_res = self.grid_resolution
        dpi = self.DPI
        image_location = final_path
        settings = QgsMapSettings()
        settings.setLayers([albedo_layer])
        settings.setBackgroundColor(QColor(255, 255, 255))
        settings.setOutputSize(QSize(grid_res, grid_res))
        settings.setExtent(aoi_extent)
        settings.setOutputDpi(dpi)
        settings.setDestinationCrs(self.height_layer.crs())
        render = QgsMapRendererParallelJob(settings)

        # if not hasattr(self, 'active_renders'):
        #     self.active_renders = []
        # self.active_renders.append(render)

        def finished():
            img = render.renderedImage()
            img.save(image_location, "png")
            self.export_finished(z_scale, min_elev, max_elev, elev_range, grid_res)

        render.finished.connect(finished)
        render.start()

